# -----------------------------------------------------------------------------------------------------------
# Menu
# -----------------------------------------------------------------------------------------------------------
yb_plugins_menu()
{
	[ "$SDEBUG" != "0" ] && echo "DEBUG: Enter for next Menu" && read dummy

    dialog --backtitle "$prgtitle" --title " $l_plugins "\
       	--cancel-label "$l_back"\
    	--ok-label "$l_choose"\
		--menu "$l_use_arrow_keys_and_enter" 14 66 9\
		"" ""\
		z "$l_back" 2>$_temp
}

# -----------------------------------------------------------------------------------------------------------
# Menu Loop
# -----------------------------------------------------------------------------------------------------------
yb_plugins()
{
	yb_plugins_doquit=false
	while [ "$yb_plugins_doquit" == "false" ]
	do
		yb_plugins_menu
	    opt=${?}
    	if [ $opt == 0 ]; then 
		    cmd=`cat $_temp`
			case "$cmd" in
				z)	yb_plugins_doquit="true"	;;
			esac
    	else
			yb_plugins_doquit=true
    	fi
	done
}
